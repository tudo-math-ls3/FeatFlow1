      SUBROUTINE CHEC4B(DCORVG,DVBDP,KVBD,KVERT,KADJ,KNPR,KMM,KNELM,
     *                  ICH5B)
      IMPLICIT DOUBLE PRECISION (A,C-H,O-U,W-Z),LOGICAL(B)
      CHARACTER SUB*6,FMT*15,CPARAM*120
      PARAMETER (NNVE=4,NNELM=1000)
      DIMENSION DCORVG(2,*),DVBDP(*)
      DIMENSION KVERT(NNVE,*),KADJ(NNVE,*),KNPR(*),KMM(2,*),KVBD(*),
     *          KNELM(*)
      COMMON /OUTPUT/ M,MT,MKEYB,MTERM,MERR,MPROT,MSYS,MTRC,IRECL8
      COMMON /ERRCTL/ IER,ICHECK
      COMMON /CHAR/   SUB,FMT(3),CPARAM
      COMMON /TRIAD/  NEL,NVT,NMT,NVE,NVEL,NBCT,NVBD
C
      COMMON /ELEMOD/ ITYPEL,NELMOD,DELMA,DELMB,KELMOD(NNELM)
C
      SAVE /OUTPUT/,/ERRCTL/,/CHAR/,/TRIAD/
C
      SUB='CHEC4B'
      IF (ICHECK.GE.997) CALL OTRC('CHEC4B','05/15/95')
C
C
C
      ICH5B=0
      DO 100 IEL=1,NEL
C
      ITYP=KNELM(IEL)
      IF (ITYP.EQ.1) THEN
       ICH5B=ICH5B+2
       GOTO 100
      ENDIF
C
      IADJ1=KADJ(1,IEL)
      IADJ2=KADJ(2,IEL)
      IADJ3=KADJ(3,IEL)
      IADJ4=KADJ(4,IEL)
C
      ITYP1=0
      IF (IADJ1.NE.0) ITYP1=KNELM(IADJ1)
      IF (ITYP1.EQ.1) THEN
       DO 110 JADJ=1,4
       IADJ=KADJ(JADJ,IADJ1)
       IF (IADJ.EQ.0) GOTO 112
110    CONTINUE
       WRITE(6,*) 'ERROR 1 IN CHEC4B'
C
112    JADJ3=MOD(JADJ+1,4)+1
       IADJH3=KADJ(JADJ3,IADJ1)
       IF (IADJH3.NE.IEL) ITYP1=0
      ELSE
       ITYP1=0
      ENDIF
C
      ITYP2=0
      IF (IADJ2.NE.0) ITYP2=KNELM(IADJ2)
      IF (ITYP2.EQ.1) THEN
       DO 120 JADJ=1,4
       IADJ=KADJ(JADJ,IADJ2)
       IF (IADJ.EQ.0) GOTO 122
120    CONTINUE
       WRITE(6,*) 'ERROR 2 IN CHEC4B'
C
122    JADJ3=MOD(JADJ+1,4)+1
       IADJH3=KADJ(JADJ3,IADJ2)
       IF (IADJH3.NE.IEL) ITYP2=0
      ELSE
       ITYP2=0
      ENDIF
C
      ITYP3=0
      IF (IADJ3.NE.0) ITYP3=KNELM(IADJ3)
      IF (ITYP3.EQ.1) THEN
       DO 130 JADJ=1,4
       IADJ=KADJ(JADJ,IADJ3)
       IF (IADJ.EQ.0) GOTO 132
130    CONTINUE
       WRITE(6,*) 'ERROR 3 IN CHEC4B'
C
132    JADJ3=MOD(JADJ+1,4)+1
       IADJH3=KADJ(JADJ3,IADJ3)
       IF (IADJH3.NE.IEL) ITYP3=0
      ELSE
       ITYP3=0
      ENDIF
C
      ITYP4=0
      IF (IADJ4.NE.0) ITYP4=KNELM(IADJ4)
      IF (ITYP4.EQ.1) THEN
       DO 140 JADJ=1,4
       IADJ=KADJ(JADJ,IADJ4)
       IF (IADJ.EQ.0) GOTO 142
140    CONTINUE
       WRITE(6,*) 'ERROR 4 IN CHEC4B'
C
142    JADJ3=MOD(JADJ+1,4)+1
       IADJH3=KADJ(JADJ3,IADJ4)
       IF (IADJH3.NE.IEL) ITYP4=0
      ELSE
       ITYP4=0
      ENDIF
C
      ITYP=ITYP1+ITYP2+ITYP3+ITYP4
C
      IF (ITYP.EQ.1) THEN
       KNELM(IEL)=2
       ICH5B=ICH5B+3
      ENDIF
C
      IF (ITYP.GE.2) THEN
       WRITE(6,*) 'ERROR 5 IN CHEC4B'
      ENDIF
C
100   CONTINUE
C
      END
