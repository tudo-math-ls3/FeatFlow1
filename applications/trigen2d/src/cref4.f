      SUBROUTINE CREF4A(DCORVG,DVBDP,KVBD,KVERT,KADJ,KNPR,KMM,KNELM,
     *                  KINDEL,NELOLD,NVTOLD)
      IMPLICIT DOUBLE PRECISION (A,C-H,O-U,W-Z),LOGICAL(B)
      CHARACTER SUB*6,FMT*15,CPARAM*120
      PARAMETER (NNVE=4,NNELM=1000)
      DIMENSION DCORVG(2,*),DVBDP(*)
      DIMENSION KVERT(NNVE,*),KADJ(NNVE,*),KNPR(*),KMM(2,*),KVBD(*),
     *          KNELM(*),KINDEL(*)
      COMMON /OUTPUT/ M,MT,MKEYB,MTERM,MERR,MPROT,MSYS,MTRC,IRECL8
      COMMON /ERRCTL/ IER,ICHECK
      COMMON /CHAR/   SUB,FMT(3),CPARAM
      COMMON /TRIAD/  NEL,NVT,NMT,NVE,NVEL,NBCT,NVBD
C
      COMMON /ELEMOD/ ITYPEL,NELMOD,DELMA,DELMB,KELMOD(NNELM)
C
      EXTERNAL PARX,PARY,TMAX
C
      SAVE /OUTPUT/,/ERRCTL/,/CHAR/,/TRIAD/
C
      SUB='CREF4A'
      IF (ICHECK.GE.997) CALL OTRC('CREF4A','05/15/95')
C
C
C
      DO 100 IEL=1,NELOLD
C
      ITYP=KNELM(IEL)
      IF (ITYP.NE.1) GOTO 100
C
      DO 105 JADJ=1,4
      IADJ=KADJ(JADJ,IEL)
      IF (IADJ.EQ.0) GOTO 107
105   CONTINUE
      WRITE(6,*) 'ERROR 1 IN CREF4A'
C
107   IVT1=KVERT(1,IEL)
      IVT2=KVERT(2,IEL)
      IVT3=KVERT(3,IEL)
      IVT4=KVERT(4,IEL)
C
      IF (JADJ.EQ.1) THEN
       IVTH1=IVT1
       IVTH2=IVT2
       IVTH3=IVT3
       IVTH4=IVT4
      ENDIF
C
      IF (JADJ.EQ.2) THEN
       IVTH1=IVT2
       IVTH2=IVT3
       IVTH3=IVT4
       IVTH4=IVT1
      ENDIF
C
      IF (JADJ.EQ.3) THEN
       IVTH1=IVT3
       IVTH2=IVT4
       IVTH3=IVT1
       IVTH4=IVT2
      ENDIF
C
      IF (JADJ.EQ.4) THEN
       IVTH1=IVT4
       IVTH2=IVT1
       IVTH3=IVT2
       IVTH4=IVT3
      ENDIF
C
      INPR1=KNPR(IVTH1)
      INPR2=KNPR(IVTH2)
      INPR3=KNPR(IVTH3)
      INPR4=KNPR(IVTH4)
C
      PX1=DCORVG(1,IVTH1)
      PX2=DCORVG(1,IVTH2)
      PX3=DCORVG(1,IVTH3)
      PX4=DCORVG(1,IVTH4)
      PY1=DCORVG(2,IVTH1)
      PY2=DCORVG(2,IVTH2)
      PY3=DCORVG(2,IVTH3)
      PY4=DCORVG(2,IVTH4)
C
      IVTS1=NVT+1
      IVTS2=NVT+2
      CALL XCORS(PX1,PX2,PY1,PY2,PXS1,PXS2,PYS1,PYS2,
     *           INPR1,INPR2,INPRS1,DELMA,DELMB)
      DCORVG(1,IVTS1)=PXS1
      DCORVG(1,IVTS2)=PXS2
      DCORVG(2,IVTS1)=PYS1
      DCORVG(2,IVTS2)=PYS2
      KNPR(IVTS1)=INPRS1
      KNPR(IVTS2)=INPRS1
      NVT=NVT+2
      CALL XCORSP(DCORVG,DVBDP,KVBD,KMM,NBCT,NVBD,IEL,
     *            IVTH1,IVTH2,IVTS1,IVTS2,INPR1,INPR2,
     *            DELMA,DELMB,PARX,PARY,TMAX)
C
      IVTS5=NVT+1
      IVTS6=NVT+2
      CALL XCORS(PX3,PX4,PY3,PY4,PXS5,PXS6,PYS5,PYS6,
     *           INPR3,INPR4,INPRS3,1D0-DELMB,1D0-DELMA)
      DCORVG(1,IVTS5)=PXS5
      DCORVG(1,IVTS6)=PXS6
      DCORVG(2,IVTS5)=PYS5
      DCORVG(2,IVTS6)=PYS6
      KNPR(IVTS5)=INPRS3
      KNPR(IVTS6)=INPRS3
      NVT=NVT+2
C
C
      KVERT(1,IEL)=IVTS1
      KVERT(2,IEL)=IVTS2
      KVERT(3,IEL)=IVTS5
      KVERT(4,IEL)=IVTS6
C
      KVERT(1,NEL+1)=IVTH1
      KVERT(2,NEL+1)=IVTS1
      KVERT(3,NEL+1)=IVTS6
      KVERT(4,NEL+1)=IVTH4
C
      KVERT(1,NEL+2)=IVTH3
      KVERT(2,NEL+2)=IVTS5
      KVERT(3,NEL+2)=IVTS2
      KVERT(4,NEL+2)=IVTH2
C
      NEL=NEL+2
C
100   CONTINUE
C
99999 END
